<h1>About</h1>

<div class="text-column">
	<h2>What is this?</h2>
	<p>
		The Interactive Scrum Guide is a web-based tool for exploring Agile and Scrum framework guides
		in an interactive way. Each guide is enriched with inline keyword tags that let you filter,
		search, and navigate directly to the topics you care about — such as
		<em>Sprint Planning</em>, <em>Product Backlog</em>, or <em>Definition of Done</em>.
	</p>

	<h2>What is it for?</h2>
	<p>
		Whether you are studying for a Scrum certification, onboarding a new team, or just need a quick
		reference during a discussion, this tool helps you find the right section across multiple guides
		in seconds. Instead of reading an entire document, pick a tag and see every paragraph that
		relates to that concept — across the Scrum Guide, Kanban Guide, Nexus Guide, Scrum@Scale, and
		more.
	</p>

